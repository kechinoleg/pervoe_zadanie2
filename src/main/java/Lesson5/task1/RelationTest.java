package Lesson5.task1;

public class RelationTest {

    public static void main(String[] args) {

        int a = 5;
        int b = 10;
        int c = 5;

        System.out.println("Результат сравнения a и b");
        System.out.println(a == b);

        System.out.println("Рузультат сравнения a и c");
        System.out.println(a == c);

        System.out.println("Результат операции a не равно b");
        System.out.println(a != b);

        System.out.println("Результат операции a >= c");
        System.out.println(a >= c);

        System.out.println("Результат операции a > c");
        System.out.println(a > c);
    }
}
